const createReviews = (productName: string, count: number, baseRating: number): ProductReview[] => {
  const authors = [
    'Alex Chen',
    'Jordan Lee',
    'Sam Rivera',
    'Taylor Kim',
    'Morgan Blake',
    'Casey Wong',
  ]
  const comments = [
    `Absolutely love my ${productName}. Build quality exceeds expectations.`,
    `Best purchase I've made this year. Highly recommend NovaTech.`,
    `Great value for the price. Fast shipping and excellent packaging.`,
    `Performance is outstanding. NovaTech never disappoints.`,
    `Sleek design and powerful specs. Worth every penny.`,
    `Customer support was helpful when I had setup questions.`,
  ]

  return Array.from({ length: Math.min(count, 6) }, (_, i) => ({
    id: `review-${i}`,
    author: authors[i % authors.length],
    rating: Math.min(5, Math.max(3, baseRating + (Math.random() > 0.5 ? 0.5 : -0.5))),
    date: new Date(2025, 10 - i, 15 - i * 2).toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
      year: 'numeric',
    }),
    comment: comments[i % comments.length],
    avatar: `https://i.pravatar.cc/80?img=${(i + 1) * 7}`,
  }))
}

